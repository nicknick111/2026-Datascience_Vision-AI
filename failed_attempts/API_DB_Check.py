# 파이썬에서 MySQL 데이터베이스에 접속하고 제어하기 위해 필요한 외부 도구(라이브러리)입니다.
import pymysql

# 파이썬 프로그램이 웹 브라우저처럼 인터넷 주소(API)로 접속해서
# 데이터를 요청할 수 있게 해주는 아주 유용한 라이브러리입니다.
import requests

# ==========================================
# 1. MySQL & API 서버 설정
# ==========================================

# DB_CONFIG는 데이터베이스(금고)에 들어가기 위한 '열쇠'들을 모아둔 사전(Dictionary)입니다.
DB_CONFIG = {
    "host": "192.168.3.21",   # 접속할 데이터베이스 컴퓨터의 IP 주소입니다.
    "database": "dronedb",    # 그 컴퓨터 안에 있는 여러 DB 중 우리가 사용할 DB의 이름입니다.
    "user": "root",           # 데이터베이스에 접속할 아이디(최고 관리자)입니다.
    "password": "1234",       # 데이터베이스 접속 비밀번호입니다.
    "port": 3306              # MySQL 데이터베이스가 통신을 위해 기본적으로 열어둔 문(포트) 번호입니다.
}

# [유지됨] 서버 상태를 확인할 때 쓰는 문서 페이지 주소
API_ENDPOINT = "http://192.168.3.21:8000/docs"

# [추가됨] ⭐️ 실제 데이터를 전송(POST)할 API 서버의 주소입니다.
# 문서를 보는 주소(/docs)와 데이터를 집어넣는 주소는 다릅니다.
# (API 개발자에게 "데이터를 등록하는 주소가 어떻게 되나요?" 라고 물어보고 이곳을 수정해야 합니다.)
API_UPLOAD_ENDPOINT = "http://192.168.3.21:8000/api/upload"

# [추가됨] ⭐️ 데이터를 가져올 DB의 테이블(엑셀의 시트 같은 개념) 이름입니다.
TARGET_TABLE = "your_table_name" # <--- 실제 사용하시는 테이블 이름으로 꼭 변경해주세요!

def check_system_status():
    """
    이 함수는 DB와 API 서버가 현재 파이썬 코드와 정상적으로 대화를 나눌 수 있는지
    상태를 진단하고 화면에 결과를 아주 쉽게 풀어서 출력해 주는 역할을 합니다.
    """
    print("\n" + "=" * 50) # 화면에 선을 그어 보기 좋게 만듭니다.
    print("[시스템 점검] 서버 연결 상태를 확인합니다...")
    print("=" * 50)
    
    # ---------------------------------------------------------
    # 1. DB 접속 상태 점검
    # ---------------------------------------------------------
    # try 구문: "일단 한 번 시도해 볼게!" 에러가 나더라도 프로그램이 멈추지 않고 except로 넘어갑니다.
    try:
        # pymysql을 사용해 DB에 연결을 시도합니다.
        # **DB_CONFIG는 위에서 만든 열쇠들을 한 번에 예쁘게 전달하는 파이썬의 편리한 문법입니다.
        # connect_timeout=3: 상대방 서버가 응답이 없을 때, 무작정 기다리지 않고 3초만 기다린 후 포기합니다.
        conn = pymysql.connect(**DB_CONFIG, connect_timeout=3)
        
        # 여기까지 에러 없이 내려왔다면 DB 통신문에 무사히 입장한 것입니다.
        print("  🟢 [DB 접속 상태] 정상: MySQL 서버 통신 가능")
        
        # 확인이 끝났으므로 DB와의 연결을 깔끔하게 끊어줍니다. (안 닫으면 서버 자원이 낭비됩니다.)
        conn.close()
        
    except Exception as e:
        # DB IP가 틀렸거나, 비밀번호가 틀렸거나, DB 서버가 꺼져있으면 이쪽으로 빠집니다.
        print(f"  🔴 [DB 접속 상태] 실패: MySQL 서버를 찾을 수 없거나 권한이 없습니다.")
        print(f"      -> 상세 원인: {e}") # 컴퓨터가 알려주는 진짜 에러 원인을 출력합니다.

    # ---------------------------------------------------------
    # 2. API 서버 접속 상태 점검
    # ---------------------------------------------------------
    try:
        # requests.get(): 설정한 주소(API_ENDPOINT)로 "화면 좀 보여줘!"라고 요청(GET)을 보냅니다.
        # timeout=3: 여기서도 무한 대기를 막기 위해 3초까지만 기다립니다.
        res = requests.get(API_ENDPOINT, timeout=3)
        
        # res.status_code는 통신 결과를 알려주는 '상태 코드'입니다.
        # 200번대 코드는 "요청하신 대로 아주 잘 처리되었습니다"라는 완벽한 성공을 의미합니다.
        if res.status_code == 200:
            print(f"  🟢 [API 접속 상태] 정상: API 서버 통신 가능 (응답 코드: 200)")
            print("      -> 확인 완료: /docs 페이지(API 명세서)가 정상적으로 열려 있습니다.")
            
        # 404 코드는 "서버랑 연결은 됐는데, 네가 찾는 /docs 페이지는 없어"라는 뜻입니다.
        elif res.status_code == 404:
            print(f"  🟡 [API 접속 상태] 주의: 서버는 켜져있으나 /docs 경로가 없습니다. (응답 코드: 404)")
            print("      -> 원인 추측: 주소 오타이거나, 서버에서 아직 문서 기능을 켜지 않았을 수 있습니다.")
            
        # 200이나 404가 아닌 다른 코드(예: 500 서버 내부 에러 등)가 떴을 때 보여줍니다.
        else:
            print(f"  🟢 [API 접속 상태] 연결됨 (응답 코드: {res.status_code})")

    except requests.exceptions.RequestException as e:
        # 아예 인터넷 주소가 잘못되었거나, 와이파이가 끊겼거나, 8000번 서버 프로그램이 안 켜져 있을 때 발생합니다.
        print(f"  🔴 [API 접속 상태] 실패: API 서버에 접속할 수 없거나 서버 프로그램이 꺼져있습니다.")
        print(f"      -> 상세 원인: {e}")
        
    print("=" * 50 + "\n")

# ==========================================
# 2. 데이터 조회 및 API 전송 함수 (새로 추가됨)
# ==========================================
def fetch_and_send_data():
    """
    DB에서 데이터를 읽어와서 API 서버로 전송하는 진짜 '일꾼' 역할을 하는 함수입니다.
    이 함수는 [DB 읽기] -> [포장하기] -> [택배 보내기(API)] 3단계로 작동합니다.
    """
    print("=" * 50)
    print("[데이터 전송 작업 시작] DB에서 데이터를 읽어 API로 보냅니다...")
    
    # ---------------------------------------------------------
    # 1단계: DB에서 데이터 가져오기 (DB 읽기)
    # ---------------------------------------------------------
    try:
        # ⭐️ 중요: cursorclass=pymysql.cursors.DictCursor
        # 데이터를 단순히 (1, '드론') 모양으로 가져오면 API가 이름(id인지 name인지)을 몰라 당황합니다.
        # DictCursor를 쓰면 {'id': 1, 'name': '드론'} 처럼 이름표가 예쁘게 붙어 나와서 API로 보내기 아주 좋습니다.
        conn = pymysql.connect(**DB_CONFIG, cursorclass=pymysql.cursors.DictCursor)
        cursor = conn.cursor()
        
        # SQL 명령어 작성 (예시: 너무 많으면 터질 수 있으니 최근 데이터 5개만 가져오기)
        # 나중에 전체 데이터를 보내려면 LIMIT 5 부분을 지우시면 됩니다.
        sql = f"SELECT * FROM {TARGET_TABLE} LIMIT 5"
        cursor.execute(sql)
        
        # fetchall()을 통해 검색된 모든 데이터를 '파이썬 리스트(목록)' 상자에 담습니다.
        data_list = cursor.fetchall()
        conn.close() # 할 일 다 했으니 DB 문을 닫습니다.
        
        if not data_list:
            print(f"  🟡 [안내] '{TARGET_TABLE}' 테이블이 텅 비어있어 보낼 데이터가 없습니다.")
            return # 데이터가 없으면 헛수고할 필요 없이 여기서 함수를 종료합니다.
            
        print(f"  🟢 [DB 읽기 성공] 총 {len(data_list)}개의 데이터를 포장했습니다.")
        
    except Exception as e:
        print(f"  🔴 [DB 읽기 실패] 데이터를 가져오는 중 에러가 발생했습니다: {e}")
        return # 에러가 나면 다음 단계로 가지 않고 멈춥니다.

    # ---------------------------------------------------------
    # 2단계: API 서버로 데이터 전송하기 (포장해서 택배 보내기)
    # ---------------------------------------------------------
    try:
        # [데이터 포장 다듬기] DB의 '날짜(datetime)' 형식은 인터넷 통신망(JSON)을 탈 때 에러를 일으킵니다.
        # 그래서 날짜 데이터가 섞여 있다면 "2024-02-04" 같은 평범한 '글자(문자열)'로 바꿔주는 작업입니다.
        for row in data_list:
            for key, value in row.items():
                if hasattr(value, 'isoformat'): 
                    row[key] = value.isoformat() 

        print("  ⏳ [API 전송 중] 포장된 데이터를 서버로 발송하는 중입니다...")
        
        # ⭐️ 핵심 통신 명령어: requests.post()
        # 눈으로 '볼 때'는 get을 쓰지만, 데이터를 꾹꾹 눌러서 '제출할 때'는 post를 씁니다.
        # json=data_list 라고 써주면, 파이썬이 알아서 세계 표준 포장지인 'JSON' 형태로 예쁘게 싸서 보냅니다.
        res = requests.post(API_UPLOAD_ENDPOINT, json=data_list, timeout=5)
        
        # 택배 수령 결과 확인
        if res.status_code in [200, 201]: # 200(완벽 성공) 또는 201(서버에 새 데이터가 잘 생성됨)
            print(f"  🟢 [API 전송 성공] 데이터가 무사히 서버 DB에 등록되었습니다! (응답 코드: {res.status_code})")
        else:
            print(f"  🔴 [API 전송 실패] 서버가 택배(데이터) 수령을 거절했습니다. (응답 코드: {res.status_code})")
            print(f"      -> 서버의 거절 사유: {res.text}") # 왜 거절했는지 서버가 남긴 편지를 읽어봅니다.
            
    except requests.exceptions.RequestException as e:
        print(f"  🔴 [API 전송 에러] 택배를 배달하러 가는 길(네트워크)에 문제가 생겼습니다: {e}")

    print("=" * 50 + "\n")

# ==========================================
# 메인 실행부
# ==========================================
if __name__ == "__main__":
    check_system_status() # 1. 기존처럼 DB와 서버가 살아있는지 먼저 진찰합니다.
    
    # [추가됨] 2. 진찰이 끝난 후, 바로 데이터를 쏠 것인지 사용자에게 물어봅니다.
    print("\n상태 점검이 완료되었습니다.")
    user_input = input("👉 DB 데이터를 추출하여 API 서버로 전송하시겠습니까? (y/n 입력 후 엔터): ")
    
    if user_input.lower() == 'y':
        fetch_and_send_data() # 사용자가 y를 누르면 위에서 만든 전송 로직이 실행됩니다!
    else:
        print("데이터 전송을 취소했습니다.")
    
    print("엔터(Enter) 키를 누르면 완전히 종료됩니다...")
